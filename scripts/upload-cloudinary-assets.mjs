const ROOT = process.cwd();
const PUBLIC_DIR = path.join(ROOT, "public");
const OUTPUT_FILE = path.join(ROOT, "src/constants/cloudinaryAssets.ts");

const CAREERS_HERO_DIR = path.join(PUBLIC_DIR, "HeroCareers-Images");
const MENTORS_BG_REMOVED_DIR = path.join(PUBLIC_DIR, "mentor's-bg-removed");
const INTERVIEW_LAPTOP_FILE = path.join(PUBLIC_DIR, "macbook-bg-less.svg");

const toSlug = (value) =>
  value
    .toLowerCase()
    .replace(/\.[^.]+$/, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

const quote = (value) => JSON.stringify(value);

async function fileExists(filePath) {
  try {
    await fs.access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function uploadImage(localPath, publicId) {
  console.log(`[upload:start] ${publicId}`);
  const result = await cloudinary.uploader.upload(localPath, {
    public_id: publicId,
    resource_type: "image",
    overwrite: true,
    invalidate: true,
  });
  console.log(`[upload:done] ${publicId} -> ${result.secure_url}`);
  return result.secure_url;
}

async function listFiles(dirPath) {
  if (!(await fileExists(dirPath))) return [];
  const entries = await fs.readdir(dirPath, { withFileTypes: true });
  return entries
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));
}

function buildConstantsFile({ laptopUrl, careersHero, mentorsBgRemoved }) {
  const careersLines = Object.entries(careersHero)
    .map(([key, url]) => `  ${quote(key)}: ${quote(url)},`)
    .join("\n");

  const mentorLines = Object.entries(mentorsBgRemoved)
    .map(([filename, url]) => `  ${quote(filename)}: ${quote(url)},`)
    .join("\n");

  return `// AUTO-GENERATED by scripts/upload-cloudinary-assets.mjs
// Re-run the script when source image files change.

export const CLOUDINARY_INTERVIEW_SUCCESS = {
  laptop: ${quote(laptopUrl)},
} as const;

export const CLOUDINARY_CAREERS_HERO = {
${careersLines}
} as const;

export const CLOUDINARY_MENTORS_BG_REMOVED = {
${mentorLines}
} as const;
`;
}

async function main() {
  console.log("[init] Starting Cloudinary asset upload script");
  console.log(`[init] Root directory: ${ROOT}`);
  const careersFiles = await listFiles(CAREERS_HERO_DIR);
  const mentorFiles = await listFiles(MENTORS_BG_REMOVED_DIR);
  console.log(`[scan] Careers hero files found: ${careersFiles.length}`);
  console.log(`[scan] Mentor bg-removed files found: ${mentorFiles.length}`);

  if (!(await fileExists(INTERVIEW_LAPTOP_FILE))) {
    throw new Error(`Missing laptop file: ${INTERVIEW_LAPTOP_FILE}`);
  }

  const laptopUrl = await uploadImage(
    INTERVIEW_LAPTOP_FILE,
    "mentorque/interview-success/macbook-bg-less"
  );
  console.log("[step] Laptop upload completed");

  const careersHero = {};
  for (const filename of careersFiles) {
    const key = toSlug(filename);
    const localPath = path.join(CAREERS_HERO_DIR, filename);
    console.log(`[careers] Uploading ${filename} as key "${key}"`);
    careersHero[key] = await uploadImage(
      localPath,
      `mentorque/careers-hero/${key}`
    );
  }
  console.log("[step] Careers hero uploads completed");

  const mentorsBgRemoved = {};
  for (const filename of mentorFiles) {
    const key = toSlug(filename);
    const localPath = path.join(MENTORS_BG_REMOVED_DIR, filename);
    console.log(`[mentors] Uploading ${filename} as key "${key}"`);
    mentorsBgRemoved[filename] = await uploadImage(
      localPath,
      `mentorque/mentors-bg-removed/${key}`
    );
  }
  console.log("[step] Mentor bg-removed uploads completed");

  const output = buildConstantsFile({ laptopUrl, careersHero, mentorsBgRemoved });
  await fs.mkdir(path.dirname(OUTPUT_FILE), { recursive: true });
  await fs.writeFile(OUTPUT_FILE, output, "utf8");
  console.log("[step] Constants file written");

  console.log(`Uploaded laptop: 1`);
  console.log(`Uploaded careers hero images: ${Object.keys(careersHero).length}`);
  console.log(`Uploaded mentor bg-removed images: ${Object.keys(mentorsBgRemoved).length}`);
  console.log(`Wrote constants: ${OUTPUT_FILE}`);
}

main().catch((error) => {
  console.error("[fatal] Cloudinary upload script failed:", error);
  process.exit(1);
});
