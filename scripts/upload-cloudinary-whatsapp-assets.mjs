const toSlug = (value) =>
  value
    .toLowerCase()
    .replace(/\.[^.]+$/, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

const quote = (value) => JSON.stringify(value);

async function fileExists(filePath) {
  try {
    await fs.access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function listFiles(dirPath) {
  if (!(await fileExists(dirPath))) return [];
  const entries = await fs.readdir(dirPath, { withFileTypes: true });
  return entries
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));
}

async function uploadAsset(localPath, publicId) {
  console.log(`[upload:start] ${publicId}`);
  const result = await cloudinary.uploader.upload(localPath, {
    public_id: publicId,
    resource_type: "auto",
    overwrite: true,
    invalidate: true,
  });
  console.log(`[upload:done] ${publicId} -> ${result.secure_url}`);
  return result.secure_url;
}

function buildConstantsFile({ screenshots, videos, thumbnails, teamPics }) {
  const screenshotsLines = screenshots.map((url) => `  ${quote(url)},`).join("\n");
  const videoLines = Object.entries(videos)
    .map(([filename, url]) => `  ${quote(filename)}: ${quote(url)},`)
    .join("\n");
  const thumbnailLines = Object.entries(thumbnails)
    .map(([filename, url]) => `  ${quote(filename)}: ${quote(url)},`)
    .join("\n");
  const teamPicLines = Object.entries(teamPics)
    .map(([filename, url]) => `  ${quote(filename)}: ${quote(url)},`)
    .join("\n");

  return `// AUTO-GENERATED by scripts/upload-cloudinary-whatsapp-assets.mjs
// Re-run when WhatsApp/testimonial media changes.

export const CLOUDINARY_WHATSAPP_SCREENSHOTS = [
${screenshotsLines}
] as const;

export const CLOUDINARY_VIDEO_TESTIMONIALS = {
${videoLines}
} as const;

export const CLOUDINARY_VIDEO_THUMBNAILS = {
${thumbnailLines}
} as const;

export const CLOUDINARY_TEAM_PICS = {
${teamPicLines}
} as const;
`;
}

async function main() {
  console.log("[init] Starting WhatsApp Cloudinary upload");

  const screenshotFiles = await listFiles(WHATSAPP_SCREENSHOTS_DIR);
  const teamPicFiles = await listFiles(TEAM_PICS_DIR);
  const thumbnailFiles = await listFiles(THUMBNAILS_DIR);

  console.log(`[scan] WhatsApp screenshot files found: ${screenshotFiles.length}`);
  console.log(`[scan] Team pic files found: ${teamPicFiles.length}`);
  console.log(`[scan] Video thumbnail files found: ${thumbnailFiles.length}`);

  const screenshots = [];
  for (const filename of screenshotFiles) {
    const localPath = path.join(WHATSAPP_SCREENSHOTS_DIR, filename);
    const key = toSlug(filename);
    screenshots.push(
      await uploadAsset(localPath, `mentorque/whatsapp-testimonials/screenshots/${key}`)
    );
  }

  const teamPics = {};
  for (const filename of teamPicFiles) {
    const localPath = path.join(TEAM_PICS_DIR, filename);
    const key = toSlug(filename);
    teamPics[filename] = await uploadAsset(localPath, `mentorque/team-pics/${key}`);
  }

  const thumbnails = {};
  for (const filename of thumbnailFiles) {
    const localPath = path.join(THUMBNAILS_DIR, filename);
    const key = toSlug(filename);
    thumbnails[filename] = await uploadAsset(
      localPath,
      `mentorque/video-testimonials/thumbnails/${key}`
    );
  }

  const videos = {};
  for (const localPath of VIDEO_TESTIMONIAL_FILES) {
    if (!(await fileExists(localPath))) continue;
    const filename = path.basename(localPath);
    const key = toSlug(filename);
    videos[filename] = await uploadAsset(
      localPath,
      `mentorque/video-testimonials/videos/${key}`
    );
  }

  const output = buildConstantsFile({ screenshots, videos, thumbnails, teamPics });
  await fs.mkdir(path.dirname(OUTPUT_FILE), { recursive: true });
  await fs.writeFile(OUTPUT_FILE, output, "utf8");

  console.log(`[done] Screenshots: ${screenshots.length}`);
  console.log(`[done] Team pics: ${Object.keys(teamPics).length}`);
  console.log(`[done] Thumbnails: ${Object.keys(thumbnails).length}`);
  console.log(`[done] Videos: ${Object.keys(videos).length}`);
  console.log(`[done] Constants written to ${OUTPUT_FILE}`);
}

main().catch((error) => {
  console.error("[fatal] WhatsApp Cloudinary upload failed:", error);
  process.exit(1);
});
